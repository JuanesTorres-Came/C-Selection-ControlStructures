/*
 * Ejemplo de estructura de control for en C.
 * Este archivo muestra cómo utilizar el bucle for para imprimir los números
 * del 1 al 5 de manera similar al while.
 *
 * Funcionalidad:
 * - El programa usa un bucle `for` para iterar y mostrar los números
 *   del 1 al 5 en la consola.
 */

#include <stdio.h>

int main() {
    // Estructura de control: for
    for (int i = 1; i <= 5; i++) {
        printf("%d\n", i);
    }

    return 0;
}
